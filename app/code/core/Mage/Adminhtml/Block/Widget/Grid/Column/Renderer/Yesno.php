class Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Yesno extends Mage_Adminhtml_Block_Widget_Grid_Column_Renderer_Abstract
{
    /**
     * Renders grid column
     *
     * @param   Varien_Object $row
     * @return  string
     */
    public function render(Varien_Object $row)
    {
        if ($row->getData($this->getColumn()->getIndex())) {
            return __('Yes');
        }
        return __('No');
    }
}