class Mage_Adminhtml_Block_Catalog_Category_Helper_Image extends Varien_Data_Form_Element_Image
{
    protected function _getUrl()
    {
        $url = false;
        if ($this->getValue()) {
            $url = Mage::getSingleton('core/store')->getConfig('catalog/images/category_upload_url')
                . $this->getValue();
        }
        return $url;
    }
}
