/**
 * Link tracker factory
 *
 * @category   Mage
 * @package    Mage_Launcher
 * @author     Magento Core Team <core@magentocommerce.com>
 */
class Mage_Launcher_Model_LinkTrackerFactory
{
    /**
     * @var Magento_ObjectManager
     */
    protected $_objectManager;

    /**
     * @param Magento_ObjectManager $objectManager
     */
    public function __construct(Magento_ObjectManager $objectManager)
    {
        $this->_objectManager = $objectManager;
    }

    /**
     * Create new link tracker model
     *
     * @param array $arguments
     * @return Mage_Launcher_Model_LinkTracker
     */
    public function create(array $arguments = array())
    {
        return $this->_objectManager->create('Mage_Launcher_Model_LinkTracker', $arguments, false);
    }
}
