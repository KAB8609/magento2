/**
 * Reviews products admin grid
 *
 * @category   Magento
 * @package    Magento_Adminhtml
 * @author     Magento Core Team <core@magentocommerce.com>
 */
class Magento_Adminhtml_Block_Catalog_Product_Edit_Tab_Reviews extends Magento_Adminhtml_Block_Review_Grid
{
    /**
     * Hide grid mass action elements
     *
     * @return Magento_Adminhtml_Block_Catalog_Product_Edit_Tab_Reviews
     */
    protected function _prepareMassaction()
    {
        return $this;
    }

    /**
     * Determine ajax url for grid refresh
     *
     * @return string
     */
    public function getGridUrl()
    {
        return $this->getUrl('*/*/reviews', array('_current' => true));
    }
}
