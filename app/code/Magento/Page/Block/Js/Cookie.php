namespace Magento\Page\Block\Js;

class Cookie extends \Magento\View\Block\Template
{
    /**
     * @var \Magento\Session\Config\ConfigInterface
     */
    protected $sessionConfig;

    /**
     * @param \Magento\View\Block\Template\Context $context
     * @param \Magento\Core\Helper\Data $coreData
     * @param \Magento\Session\Config\ConfigInterface $sessionConfig
     * @param array $data
     */
    public function __construct(
        \Magento\View\Block\Template\Context $context,
        \Magento\Core\Helper\Data $coreData,
        \Magento\Session\Config\ConfigInterface $sessionConfig,
        array $data = array()
    ) {
        $this->sessionConfig = $sessionConfig;
        parent::__construct($context, $coreData, $data);
    }

    /**
     * Get configured cookie domain
     *
     * @return string
     */
    public function getDomain()
    {
        $domain = $this->sessionConfig->getCookieDomain();
        if (!empty($domain[0]) && ($domain[0] !== '.')) {
            $domain = '.' . $domain;
        }
        return $domain;
    }

    /**
     * Get configured cookie path
     *
     * @return string
     */
    public function getPath()
    {
        return $this->sessionConfig->getCookiePath();
    }
}
